#include<stdio.h>
int main()
{
    int a=-24; //has neg and positive values both
    int b=24;
    unsigned int c=1244; //unsigned means only positive values
    long int d=124L;
    long long int e=124LL;//suffix used here as well
    printf("%d %d %u %ld %lld",a,b,c,d,e);
    return 0;
}    




